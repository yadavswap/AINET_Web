            <h4>Present Status of the Project </h4><br>
            <p align=" justify">
                    AINET has put together a team of volunteer teachers, called AINET Connect Associates, creating an
                    impactful presence for AINET on various social media platforms by reaching out to geographically
                    scattered and vast ELT community with diverse professional development activities. The project
                    team and 14 AINET Connect Associates, all volunteer teacher-members of AINET, supported by the
                    AINET executive members and the partners, have been designing, delivering, and monitoring
                    initiatives and activities on social media platforms. We started with Facebook, Twitter, WhatsApp
                    and YouTube and have subsequently added Pinterest, Telegram and Instagram, in addition to
                    revamping the AINET website as a forum for exchange, dissemination and interaction.
            </p>
