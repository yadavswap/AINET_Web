    <div class="container">
            <h4>Future Plans of the Project</h4><br>
            <p align=" justify">
                    Within the larger team of the AINET Connect Associates, we are consolidating smaller groups or
                    pairs of teachers that will hold responsibility of looking after different social media platforms, which
                    they will manage for longer durations. The two face-to-face meetings-cum-workshops spread
                    through the year were crucial in capacity building of the AINET Connect Associates. This is expected
                    to further strengthen the reach and impact of AINET, enhance its organisational capabilities and
                    benefit its membership (and wider ELT community) in terms of professional development. Efforts
                    are underway to expand these teams with the addition of new volunteers. This is in order to build a
                    potential second line of leadership, besides ensuring a wider participatory management of AINET
                    activities and building on the expertise available within AINET. It is also proposed to prepare a draft
                    social media policy, as well as a manual/ handbook on social media management, for teacher
                    associations based on the experience and learnings of the project, which will be highly useful guiding
                    documents not only for AINET but also for other teacher associations.
